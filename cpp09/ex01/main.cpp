#include "RPN.hpp"

int main(int ac, char **av)
{
	if (ac != 2)
		return (1);
	RPN rpn;
	try
	{
		rpn.doOperations(av[1]);
		std::cout << rpn.getResult() << std::endl;
	}
	catch(const std::exception& e)
	{
		std::cerr << e.what() << std::endl;
	}
	
	return (0);
}
